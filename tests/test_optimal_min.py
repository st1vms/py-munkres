        solvable = True
        disallowment_map = {}
        if len(test_case) == 3:
            cost_matrix, exp_assignments, exp_cost = test_case
        elif len(test_case) == 4:
            cost_matrix, exp_assignments, exp_cost, disallowment_map = test_case
